package dynamicPhilo;

public class Oeuvre {
	
	int id_oeuvre;
	String titre;
	String annee_de_parution;
	int id;
	
	public int getId_oeuvre() {
		return id_oeuvre;
	}
	public void setId_oeuvre(int id_oeuvre) {
		this.id_oeuvre = id_oeuvre;
	}
	public String getTitre() {
		return titre;
	}
	public void setTitre(String titre) {
		this.titre = titre;
	}
	public String getAnnee_de_parution() {
		return annee_de_parution;
	}
	public void setAnnee_de_parution(String annee_de_parution) {
		this.annee_de_parution = annee_de_parution;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}

}
